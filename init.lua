-- Godot: start pipe server when project.godot is found nearby
local cwd = vim.fn.getcwd()
for _, suffix in ipairs({ '/', '/../' }) do
  if vim.uv.fs_stat(cwd .. suffix .. 'project.godot') then
    local pipe = cwd .. suffix .. 'server.pipe'
    if not vim.uv.fs_stat(pipe) then
      vim.fn.serverstart(pipe)
    end
    break
  end
end
